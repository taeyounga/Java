package java_20170925;

public class ArrayVO {
	String name, city;
	public ArrayVO() {}
	public ArrayVO(String name, String city) {
		this.name=name;
		this.city=city;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	
}
